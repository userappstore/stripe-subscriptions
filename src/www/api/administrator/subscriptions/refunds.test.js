/* eslint-env mocha */
const assert = require('assert')
const TestHelper = require('../../../../../test-helper.js')

describe('/api/administrator/subscriptions/refunds', () => {
  describe('Refunds#GET', () => {
    it('should limit refunds to one page', async () => {
      const administrator = await TestHelper.createAdministrator()
      const product = await TestHelper.createProduct(administrator, {published: true})
      const plan1 = await TestHelper.createPlan(administrator, {productid: product.id, published: true, trial_period_days: 0, amount: 1000})
      const plan2 = await TestHelper.createPlan(administrator, {productid: product.id, published: true, trial_period_days: 0, amount: 2000})
      const plan3 = await TestHelper.createPlan(administrator, {productid: product.id, published: true, trial_period_days: 0, amount: 3000})
      const user1 = await TestHelper.createUser()
      await TestHelper.createCustomer(user1)
      await TestHelper.createCard(user1)
      await TestHelper.createSubscription(user1, plan1.id)
      await TestHelper.waitForWebhooks(2)
      await TestHelper.loadCharge(user1, user1.subscription.id)
      await TestHelper.createRefund(user1, user1.charge)
      await TestHelper.waitForWebhooks(3)
      const user2 = await TestHelper.createUser()
      await TestHelper.createCustomer(user2)
      await TestHelper.createCard(user2)
      await TestHelper.createSubscription(user2, plan2.id)
      await TestHelper.waitForWebhooks(5)
      await TestHelper.loadCharge(user2, user2.subscription.id)
      const refund2 = await TestHelper.createRefund(user2, user2.charge)
      await TestHelper.waitForWebhooks(6)
      const user3 = await TestHelper.createUser()
      await TestHelper.createCustomer(user3)
      await TestHelper.createCard(user3)
      await TestHelper.createSubscription(user3, plan3.id)
      await TestHelper.waitForWebhooks(8)
      await TestHelper.loadCharge(user3, user3.subscription.id)
      const refund3 = await TestHelper.createRefund(user3, user3.charge)
      await TestHelper.waitForWebhooks(9)
      const req = TestHelper.createRequest(`/api/administrator/subscriptions/refunds`, 'GET')
      req.administratorAccount = req.account = administrator.account
      req.administratorSession = req.session = administrator.session
      const refunds = await req.route.api.get(req)
      assert.equal(refunds.length, 2)
      assert.equal(refunds[0].id, refund3.id)
      assert.equal(refunds[1].id, refund2.id)
    })
  })
})
