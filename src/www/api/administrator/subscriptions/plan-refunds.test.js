/* eslint-env mocha */
const assert = require('assert')
const TestHelper = require('../../../../../test-helper.js')

describe('/api/administrator/subscriptions/plan-refunds', () => {
  describe('PlanRefunds#GET', () => {
    it('should limit refunds on plan to one page', async () => {
      const administrator = await TestHelper.createAdministrator()
      const product = await TestHelper.createProduct(administrator, {published: true})
      await TestHelper.createPlan(administrator, {productid: product.id, published: true, trial_period_days: 0, amount: 1000})
      const user = await TestHelper.createUser()
      await TestHelper.createCustomer(user)
      await TestHelper.createCard(user)
      await TestHelper.createSubscription(user, administrator.plan.id)
      await TestHelper.waitForWebhooks(2)
      await TestHelper.loadCharge(user, user.subscription.id)
      await TestHelper.createRefund(user, user.charge)
      await TestHelper.waitForWebhooks(3)
      const user2 = await TestHelper.createUser()
      await TestHelper.createCustomer(user2)
      await TestHelper.createCard(user2)
      await TestHelper.createSubscription(user2, administrator.plan.id)
      await TestHelper.waitForWebhooks(5)
      await TestHelper.loadCharge(user2, user2.subscription.id)
      const refund2 = await TestHelper.createRefund(user2, user2.charge)
      await TestHelper.waitForWebhooks(6)
      const user3 = await TestHelper.createUser()
      await TestHelper.createCustomer(user3)
      await TestHelper.createCard(user3)
      await TestHelper.createSubscription(user3, administrator.plan.id)
      await TestHelper.waitForWebhooks(8)
      await TestHelper.loadCharge(user3, user3.subscription.id)
      const refund3 = await TestHelper.createRefund(user3, user3.charge)
      await TestHelper.waitForWebhooks(9)
      const req = TestHelper.createRequest(`/api/administrator/subscriptions/plan-refunds?planid=${administrator.plan.id}`, 'GET')
      req.administratorAccount = req.account = administrator.account
      req.administratorSession = req.session = administrator.session
      req.plan = administrator.plan
      const subscriptions = await req.route.api.get(req)
      assert.equal(subscriptions.length, 2)
      assert.equal(subscriptions[0].id, refund3.id)
      assert.equal(subscriptions[1].id, refund2.id)
    })
  })
})
